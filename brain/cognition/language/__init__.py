# Orrin's native language faculty — grown from his own experience, not an LLM.
#
# This package is the NEURAL language organ:
#   tokenizer.py   — his own subword vocabulary
#   native_lm.py   — his from-scratch transformer (trains, generates, evaluates)
#   library.py     — the reading corpus (fetch / browse / pick / read)
#   acquisition.py — the developmental loop (what he learns from, incl. grounding,
#                    his own dialogue, and his learned vocabulary)
#   voice.py       — the gated handoff that lets the organ take over his speech
#
# Distinct from the SYMBOLIC phrasing tier in cognition/language_acquisition.py
# (single file), which only mines discourse-marker openers for the template
# speech builder. That one helps how templates phrase things today; this package
# is the organ meant to become his actual voice. They are complementary layers.
