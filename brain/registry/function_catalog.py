def _subsystem_for(rel_path: str) -> str:
    p = rel_path.replace("\\", "/").lower()
    for frag, name in _SUBSYSTEM_RULES:
        if frag in p:
            return name
    return "Other"


def _unwrap(fn: Callable) -> Callable:
    try:
        return inspect.unwrap(fn)
    except ValueError:  # intentional: unwrap cycle → use wrapper as-is
        return fn


def _introspect(fn: Callable) -> Optional[Dict[str, Any]]:
    fn = _unwrap(fn)
    try:
        src_file = inspect.getsourcefile(fn) or inspect.getfile(fn)
    except (TypeError, OSError):
        return None
    if not src_file:
        return None
    try:
        rel = str(Path(src_file).resolve().relative_to(_REPO_ROOT))
    except Exception:
        rel = Path(src_file).name
    lineno = getattr(getattr(fn, "__code__", None), "co_firstlineno", 0) or 0
    endline = lineno
    try:
        lines, start = inspect.getsourcelines(fn)
        lineno = start or lineno
        endline = (start or lineno) + max(0, len(lines) - 1)
    except (TypeError, OSError):
        pass
    doc = (inspect.getdoc(fn) or "").strip()
    summary = doc.splitlines()[0].strip() if doc else ""
    return {"file": rel, "lineno": int(lineno), "endline": int(endline), "summary": summary[:200]}


def _iter_registry() -> List[Tuple[str, Callable, str]]:
    """Yield (name, callable, kind) for cognitive + behavioral functions."""
    out: List[Tuple[str, Callable, str]] = []
    try:
        from brain.registry.cognition_registry import COGNITIVE_FUNCTIONS
        for name, meta in (COGNITIVE_FUNCTIONS or {}).items():
            fn = meta.get("function") if isinstance(meta, dict) else meta
            if callable(fn) and isinstance(name, str):
                out.append((name, fn, "cognition"))
    except Exception as e:
        _log.warning("catalog: cognition registry unavailable: %s", e)
    try:
        from brain.registry.behavior_registry import BEHAVIORAL_FUNCTIONS
        for name, meta in (BEHAVIORAL_FUNCTIONS or {}).items():
            fn = meta.get("function") if isinstance(meta, dict) else meta
            if callable(fn) and isinstance(name, str):
                out.append((name, fn, "behavior"))
    except Exception as e:
        _log.warning("catalog: behavior registry unavailable: %s", e)
    return out


_CACHE: Optional[Dict[str, Any]] = None


def build_catalog(force: bool = False) -> Dict[str, Any]:
    """
    Return {"functions": {name: {subsystem, file, lineno, endline, summary, kind}},
            "subsystems": {subsystem: [names...]}}. Cached after first build.
    """
    global _CACHE
    if _CACHE is not None and not force:
        return _CACHE
    functions: Dict[str, Any] = {}
    subsystems: Dict[str, List[str]] = {}
    for name, fn, kind in _iter_registry():
        if name in functions:
            continue
        info = _introspect(fn)
        if not info:
            continue
        sub = _subsystem_for(info["file"])
        functions[name] = {**info, "subsystem": sub, "kind": kind, "name": name}
        subsystems.setdefault(sub, []).append(name)
    for names in subsystems.values():
        names.sort()
    _CACHE = {"functions": functions, "subsystems": subsystems}
    return _CACHE
