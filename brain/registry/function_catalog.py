#
# Builds a "map of Orrin's mind": every registered cognitive (and behavioral)
# function tagged with the subsystem it belongs to, where its source lives, and a
# one-line summary. Drives the Cognitive Map UI (zoomable code map + click-to-read).
#
# Pure introspection over the live registries — no behaviour change.
